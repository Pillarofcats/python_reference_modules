#DICT METHODS
def dictMethods():
  print("|| DICT METHODS ||\n")

  aDict = {
    "name": "Jacob",
    "age": 34
  }

  print(f"Dict: {aDict}")
  print(f"Dict get name: {aDict['name']}")
  print(f"Dict get age: {aDict['age']}")
  print(f"Dict keys: {aDict.keys()}")
  print(f"Dict values: {aDict.values()}")
  print(f"Dict items: {aDict.items()}")

  print("")