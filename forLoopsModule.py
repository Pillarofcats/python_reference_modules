#FOR LOOPS
def forLoops():
  print("|| FOR LOOPS ||\n")

  names = ["Jacob", "Dan", "Michael"]

  for name in names:
    print(f"list name: {name}")
    
  print("")
    
  for i in range(len(names)):
    print(f"index range(len(names)): {i}")
    
  print("")
    
  for i in range(2, 5):
    print(f"index range(2-5): {i}")
    
  print("")
    
  for i in range(0, 9, 2):
    print(f"index range(0-9, increment 2): {i}")
    
  print("")